package com.mycompany.operatorpenambah;

/**
 *
 * @author DIAN
 */
public class OperatorPenambah {

    public static void main(String[] args) {
        int a, b;
        a = 7;
        b = 3;
        
        System.out.println("Nilai A = " + a);
        System.out.println("Nilai ++A = " + ++a);
        System.out.println("Nilai A++ = " + a++);
        System.out.println("Nilai A = " + a);
        System.out.println("--------------");
        System.out.println("Nilai B = " + b);
        System.out.println("Nilai ++B = " + ++b);
        System.out.println("Nilai B++ = " + b++);
        System.out.println("Nilai B = " + b);
    }
    
}
